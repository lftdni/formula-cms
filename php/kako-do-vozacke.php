                                <div class="post-excerpt">
<p> <b> 1. Liječnički pregled </b> <br/> Prije upisa u autoškolu dužni ste obaviti liječnički pregled i dobiti Uvjerenje o tjelesnoj i duševnoj sposobnosti za upravljanje vozilom određene kategorije.
Cijena liječničkog pregleda je 430,00 kn za A, A1 i B kategoriju. Pregled možete obaviti u ordinacijama koje su specijalizirane za obavljanje ove djelatnosti.
Od 1.siječnja 2011.g. potrebno je na pregled donijeti potvrdu od obiteljskog liječnika da ne bolujete od kroničnih bolesti (povijest bolesti). Liječnička svjedodžba za obuku u autoškoli i polaganje ispita važi neograničeno, a za Policijsku upravu kod podizanja vozačke dozvole ista ne smije biti starija od 15 mjeseci.
 </p>
 <p> <b>2. Upis u autoškolu</b> <br/>
 Nakon tog vrlo bitnog dijela s liječničkim uvjerenjem o duševnoj i tjelesnoj sposobnosti za upravljanje vozilom i osobnom iskaznicom pristupate upisu u autoškolu i potpisujete ugovor o osposobljavanju kandidata za vozača u kojem su navedeni svi bitni članci kako bi se i vi i autoškola obvezali o poštivanju obveza tijekom polaganja vozačkog ispita.
Maloljetni kandidati na upis moraju doći s roditeljem ili starateljem.
 </p>
 
 <p><b>3. Početak osposobljavanja kandidata</b> <br/>
 Ispit iz nastavnog predmeta Prometni propisi i sigurnosna pravila kandidat je dužan položiti u roku od godine dana od dana završetka osposobljavanja iz tog predmeta.
Kandidat koji ispit iz nastavnog predmeta Prometni propisi i sigurnosna pravila ne uspije položiti u navedenom roku ili taj ispit ne savlada niti nakon petog pokušaja u roku od godine dana od dana završetka osposobljavanja, mora ponovno proći osposobljavanje iz toga nastavnog predmeta.
Nakon uspješno odslušanih predavanja iz PPSP-a slijedi prisustvovanje predavanju iz predmeta Pružanje prve pomoći (PPP). Ta predavanja traju 9 nastavnih sati i provodi ih isključivo Crveni križ.
Praktična obuka iz vožnje traje najmanje 35 nastavnih sati. Prije završetka obuke iz predmeta Upravljanje vozilom (UV) potrebno je položiti i ispit iz predmeta Pružanje prve pomoći (PPP). Sve ispite provodi Hrvatski autoklub (HAK).
 </p>
 
 <p> <b>4. Ispit iz predmeta Upravljanje vozilom</b> <br/>
 Nakon što ste položili ispite iz Prometnih propisa i Prve pomoći ostaje vam još jedan korak do vozačke dozvole - ispit iz predmeta Upravljanje vozilom (UV).
Ispit se sastoji od dva dijela. Prvi dio se provodi na prometnom vježbalištu (poligonu), a drugi dio ispita na javnoj cesti.
Ispitu iz ovog predmeta pristupite tek nakon razgovora s instruktorom vožnje jer on kao stručna osoba može procijeniti da li je razina vaših stečenih vještina dovoljna da uspješno savladate ovaj ispit.
 </p>
 
 <p> <b>5. IZDAVANJE VOZAČKE DOZVOLE</b> <br/>
 Izdavanje se vrši u Policijskoj upravi prema mjestu stanovanja. Uz uvjerenje o položenom vozačkom ispitu i uz važeće liječničko (ne smije biti starije od 15 mjeseci) možete podići svoju vozačku dozvolu. 
 </p>
 
 <p class="text-center"> <b>Važno</b> <br/>
 Ispit  "Upravljanja vozilom" (UV) trebali biste položiti u roku od najdulje 18 mjeseci od datuma kad ste položili ispit "Prometnih propisa".
Nakon isteka tog roka potrebno je, prije izlaska na ispit iz vožnje, provesti dopunsko osposobljavanje za upravljanje vozilom u trajanju od najmanje 12 dodatnih sati (po pravilniku o načinu obavljanja i organiziranja vozačkih ispita).

 </p>
							
